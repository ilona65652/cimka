<?php
// Создаем многомерный массив с расписанием уроков
$schedule = array(
    "Понедельник" => array("Математика", "Физика", "Литература"),
    "Вторник" => array("История", "Химия", "Английский язык"),
    "Среда" => array("Физкультура", "Музыка", "География"),
    "Четверг" => array("Русский язык", "Биология", "Информатика"),
    "Пятница" => array("Обществознание", "Изобразительное искусство", "Технология")
);

// Выводим расписание на экран, используя циклы
foreach ($schedule as $day => $lessons) {
    echo $day . ":\n";
    foreach ($lessons as $lesson) {
        echo "- " . $lesson . "\n";
    }
    echo "\n";
}
?>